// Helper function to get nested translations (for categories)
const getNestedValue = (obj, path) => {
  return path.split('.').reduce((current, key) => current?.[key], obj)
}

export function t(key, locale = 'en') {
  // Try exact match
  let value = translations[locale]?.[key]
  
  // If not found, try nested (for categories like 'expenseCategories.office')
  if (!value && key.includes('.')) {
    value = getNestedValue(translations[locale], key)
  }
  
  // Fallback to English
  if (!value) {
    value = translations.en[key]
    if (!value && key.includes('.')) {
      value = getNestedValue(translations.en, key)
    }
  }
  
  // Last resort: return the key itself
  return value || key
}

